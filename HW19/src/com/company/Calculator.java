package com.company;

public class Calculator {
    static int add(int a, int b){
        return a + b;
    }
    static int mult(int a, int b){
        return a * b;
    }
    static int div(int a, int b){
        return a / b;
    }
    static int subt(int a, int b) {
        return a - b;
    }
}
